// Inicialização quando o DOM estiver carregado
document.addEventListener('DOMContentLoaded', function() {
    carregarProducoes();
    configurarMenuMobile();
    configurarScrollButton();
    configurarModal();
});

// Função para carregar as produções na página
function carregarProducoes() {
    const grid = document.querySelector('.producoes-grid');
    grid.innerHTML = '';
    
    producoes.forEach(producao => {
        const card = document.createElement('div');
        card.className = 'producao-card';
        card.setAttribute('data-categoria', producao.categoria);
        
        card.innerHTML = `
            <div class="producao-imagem">
                <img src="${producao.imagem}" alt="${producao.titulo}">
            </div>
            <div class="producao-info">
                <span class="producao-categoria">${formatarCategoria(producao.categoria)}</span>
                <h3 class="producao-titulo">${producao.titulo}</h3>
                <p class="producao-descricao">${producao.descricao}</p>
                <button class="producao-btn" data-id="${producao.id}">Ver mais</button>
            </div>
        `;
        
        grid.appendChild(card);
    });
    
    // Adicionar event listeners aos botões "Ver mais"
    document.querySelectorAll('.producao-btn').forEach(btn => {
        btn.addEventListener('click', function() {
            const id = parseInt(this.getAttribute('data-id'));
            abrirModal(id);
        });
    });
}

// Função para formatar a categoria para exibição
function formatarCategoria(categoria) {
    const categorias = {
        'redacao': 'Redação',
        'criativo': 'Texto Criativo'
    };
    return categorias[categoria] || categoria;
}

// Função para configurar o menu mobile
function configurarMenuMobile() {
    const menuToggle = document.getElementById('mobile-menu');
    const navUl = document.querySelector('nav ul');
    
    menuToggle.addEventListener('click', function() {
        this.classList.toggle('active');
        navUl.classList.toggle('active');
    });
    
    document.querySelectorAll('nav a').forEach(link => {
        link.addEventListener('click', function() {
            menuToggle.classList.remove('active');
            navUl.classList.remove('active');
        });
    });
}

// Função para configurar o botão de scroll
function configurarScrollButton() {
    const scrollButton = document.getElementById('scroll-to-producoes');
    scrollButton.addEventListener('click', function() {
        document.getElementById('producoes').scrollIntoView({
            behavior: 'smooth'
        });
    });
}

// Função para configurar o modal
function configurarModal() {
    const modal = document.getElementById('modal-producao');
    const closeBtn = document.querySelector('.close-modal');
    
    closeBtn.addEventListener('click', function() {
        modal.style.display = 'none';
    });
    
    window.addEventListener('click', function(event) {
        if (event.target === modal) {
            modal.style.display = 'none';
        }
    });
    
    document.addEventListener('keydown', function(event) {
        if (event.key === 'Escape' && modal.style.display === 'block') {
            modal.style.display = 'none';
        }
    });
}

// Função para abrir o modal com os detalhes da produção
function abrirModal(id) {
    const producao = producoes.find(p => p.id === id);
    const modal = document.getElementById('modal-producao');
    const modalBody = document.querySelector('.modal-body');
    
    if (producao) {
        modalBody.innerHTML = `
            <img src="${producao.imagem}" alt="${producao.titulo}" class="modal-imagem">
            <h3 class="modal-titulo">${producao.titulo}</h3>
            <span class="modal-categoria">${formatarCategoria(producao.categoria)}</span>
            <p class="modal-descricao">${producao.descricao}</p>
            <div class="modal-texto">
                ${producao.textoCompleto}
            </div>
        `;
        modal.style.display = 'block';
    }
}